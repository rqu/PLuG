package ch.usi.dag.disl.exception;

public class AnnotParserException extends DiSLException {

	private static final long serialVersionUID = -2826083567381934062L;

	public AnnotParserException() {
		super();
	}

	public AnnotParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public AnnotParserException(String message) {
		super(message);
	}

	public AnnotParserException(Throwable cause) {
		super(cause);
	}
}
