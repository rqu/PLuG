package ch.usi.dag.disl.exception;

public class ASMException extends DiSLException {

	private static final long serialVersionUID = -2081564817371378076L;

	public ASMException() {
		super();
	}

	public ASMException(String message, Throwable cause) {
		super(message, cause);
	}

	public ASMException(String message) {
		super(message);
	}

	public ASMException(Throwable cause) {
		super(cause);
	}
}
